package SelectionStatements;

public class IfElseIf {
    public static void main(String[] args) {
        int no1 =10;
        if(no1 >1){
            System.out.println("hello1");
        } if (no1>5) {
            System.out.println("hello2");
        } else if (no1>10) {
            System.out.println("hello3");
        }else {
            System.out.println("hello4");
        }
    }
}
